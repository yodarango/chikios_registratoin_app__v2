export function requestLogger(req, res, next) {
  console.log(`
  ************************************************************
  * METHOD: ${req.method}                                    *
  * -------------------------------------------------------- *
  * BODY: ${JSON.stringify(req.body)}                        *
  * -------------------------------------------------------- *
  * URL: ${req.url}                                          *
  ************************************************************
  `);
  next();
}
